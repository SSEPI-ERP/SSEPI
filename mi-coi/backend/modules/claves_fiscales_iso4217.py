def codigo_desde_combo(valor: str) -> str:
    """Extrae ISO (3 letras) desde 'EUR=Euro' o 'EUR'."""
    s = (valor or "").strip()
    if "=" in s:
        return s.split("=", 1)[0].strip().upper()[:3]
    return s.upper()[:3] if len(s) >= 3 else "MXN"
